# პაროლი, რომელსაც უნდა შეესაბამებოდეს მომხმარებლის შეყვანილი მნიშვნელობა
correct_password = "mypassword"  # ჩანაცვლეთ თქვენი პაროლით

# ინიციალიზაცია ცვლადისთვის, რომელიც დაითვლის მცდელობების რაოდენობას
attempts = 0

while True:
    # მომხმარებლის შეყვანილი პაროლი
    entered_password = input("შეიყვანეთ პაროლი: ")
    attempts += 1  # თითოეულ ციკლზე მცდელობების გაზრდა
    
    if entered_password == correct_password:
        print("Access granted!")  # წვდომის დაშვება
        break  # ციკლის შეჩერება
    else:
        print("Incorrect password. სცადეთ თავიდან.")

# დაბეჭდეთ, რამდენჯერ სცადა მომხმარებელმა
print(f"თქვენ სულ {attempts} მცდელობა დაგჭირდათ პაროლის სწორად შეყვანისთვის.")
