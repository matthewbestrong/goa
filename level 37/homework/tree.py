# Creating a tuple with repeated elements
my_tuple = (10, 20, 30, 20, 40, 20, 50)

# Using .count() to count occurrences of 20
count_20 = my_tuple.count(20)

# Using .index() to find the first occurrence of 20
first_index_20 = my_tuple.index(20)

# Printing the results
print("Count of 20:", count_20)
print("First occurrence of 20 at index:", first_index_20)