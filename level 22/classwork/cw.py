# სია 10 ელემენტით
my_list = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']

# მომხმარებლიდან რიცხვის შემოტანა
user_input = input("შეიყვანეთ რიცხვი: ")

# ვამოწმებთ, რომ რიცხვი მთელი რიცხვია
try:
    index = int(user_input)  # რიცხვი გარდაქმნისას მთლიანად ინახება
    
    # სიის სიგრძე
    length = len(my_list)
    
    # პირობა 1: დადებითი ინდექსი
    if 0 <= index < length:
        print(f"ელემენტი ინდექსზე {index}: {my_list[index]}")
    
    # პირობა 2: უარყოფითი ინდექსი
    elif -length <= index <= -1:
        print(f"ელემენტი უარყოფითი ინდექსზე {index}: {my_list[index]}")
    
    # სხვა შემთხვევა
    else:
        print("მითითებული ინდექსი სცდება სიის საზღვრებს.")
except ValueError:
    print("შეიყვანეთ მხოლოდ მთელი რიცხვი!")
